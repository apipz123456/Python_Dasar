#Aritmatika dasar
a = 10
b = 12

#pertambahan
hasil = a + b 
print(hasil)

#pengurangan
hasil = a - b
print(hasil)

#pengalian
hasil = a * b
print(hasil)

#pembagian
hasil = a / b
print(hasil)

#pangkat
hasil = a ** b
print(hasil)

#modulus(sisa bagi)
hasil = a % b
print(hasil)

#flor devision
hasil = a // b
print(hasil)

#prioritas
hasil = a * b // b ** a % a - (a + b + b)
print("hasilnya : ",hasil)